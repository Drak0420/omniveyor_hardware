COMMENT ON COLUMN OMNIVEYORS.CARTMAN0X_MOTOR.ROLL3AMP IS 'Current through Roll Motor #3 (A).';
COMMENT ON COLUMN OMNIVEYORS.CARTMAN0X_MOTOR.STEER4AMP IS 'Current through Steer Motor #4 (A).';
