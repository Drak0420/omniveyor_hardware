-- search and replace all CARTMAN0X with X equal to each robot ID, to create table sets in the database.

CREATE TABLE OMNIVEYORS.CARTMAN0X_MOTOR (
	TIMEENTRY TIMESTAMP(3) DEFAULT CURRENT_TIMESTAMP(3),
	STEER1AMP FLOAT(24),
	ROLL1AMP FLOAT(24),
	STEER2AMP FLOAT(24),
	ROLL2AMP FLOAT(24),
	STEER3AMP FLOAT(24),
	ROLL3AMP FLOAT(24),
	STEER4AMP FLOAT(24),
	ROLL4AMP FLOAT(24),
	STEER1AMAX FLOAT(24),
	ROLL1AMAX FLOAT(24),
	STEER2AMAX FLOAT(24),
	ROLL2AMAX FLOAT(24),
	STEER3AMAX FLOAT(24),
	ROLL3AMAX FLOAT(24),
	STEER4AMAX FLOAT(24),
	ROLL4AMAX FLOAT(24)
)
TABLESPACE DATA;
COMMENT ON COLUMN OMNIVEYORS.CARTMAN0X_MOTOR.TIMEENTRY IS 'Time at which data is measured.';
COMMENT ON COLUMN OMNIVEYORS.CARTMAN0X_MOTOR.STEER1AMP IS 'Current through Steer Motor #1 (A).';
